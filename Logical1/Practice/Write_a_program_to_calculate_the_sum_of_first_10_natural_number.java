class Write_a_program_to_calculate_the_sum_of_first_10_natural_number 
{
	public static void main(String[] args) 
	{
		int naturalNumber=1;
		int sumOfNaturalNumber=0;
		while(naturalNumber<=10)
		{
			sumOfNaturalNumber=sumOfNaturalNumber+naturalNumber;
             naturalNumber++;
		}
		System.out.println("sumOfNaturalNumber:"+sumOfNaturalNumber);
	}
}
