package com.app.oop;

public class EmployeeInEncapsulation1 
  {
     private String empName;
     private int empId;
     private float empSallery;
     private String empAddresh;
     
     public void setEmpName(String empName)
     {
    	 this.empName=empName;
     }
     public String getEmpName()
     {
    	 return empName;
     }
     public void setEmpId(int empId)
     {
    	 this.empId=empId;
     }
     public int getEmpId()
     {
    	 return empId;
     }
     public void setEmpSallery(float empSallery)
     {
    	 this.empSallery=empSallery;
     }
     public float getEmpSallery()
     {
    	 return empSallery;
     }
     public void setEmpAddresh(String empAddresh)
     {
    	 this.empAddresh=empAddresh;
     }
     public String getEmpAddresh()
     {
    	return empAddresh; 
     }
  }
