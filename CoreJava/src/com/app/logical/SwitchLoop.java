package com.app.logical;

public class SwitchLoop 
{

	public static void main(String[] args)
	{
		int no=0;
		switch(no)
		{
		case 0:
		    System.out.println("zero");
		    break;
		case 4:
			System.out.println("four");
			break;
		case 8:
			System.out.println("eight");
			break;
		 default:
			 System.out.println("default");
		}

	}

}
