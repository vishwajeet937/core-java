package Operators;

public class Pre_increment
{

	public static void main(String[] args)
	{
		int a=10;
		int b=a++;
		int c=++a;
		int d=a--;
		int e=--a;
		System.out.println("B increment is:"+b);
		System.out.println("C increment is:"+c);
		System.out.println("d decrement is:"+d);
		System.out.println("e decrement is:"+e);

	}

}
